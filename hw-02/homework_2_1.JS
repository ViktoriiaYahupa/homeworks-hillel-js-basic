"number" + 23 + 32;
/**
 * результат --> 'number2332'
 *
 * конкатенація (якщо хоча б один з операндів рядок ("string"), то отримаємо рядок)
 */

41 + 1 + "number";
/**
 * результат --> '42number'
 *
 * конкатенація (якщо хоча б один з операндів рядок ("string"), то отримаємо рядок)
 * послідовність дій: 41+1=42
 *                    42+'number'='42number'
 */

null + 1;
/**
 * результат --> 1
 *
 * null це пусте значення, яке конвертується в 0
 * 0+1=1
 */

"five" + +"two";
/**
 *  результат --> 'fiveNaN'
 *
 * в даному прикладі маємо унарний плюс і конкатенацію
 * унарний плюс намагається перетворити рядок в число (в даному прикладі це не можливо)
 *              + 'two' --> NaN
 *              'five' + NaN
 */

2 && 7;
/**
 * результат --> 7
 *
 * логічний оператор && повертає перше хибне (false) значення
 * якщо всі значення true, то повертається значення останнього операнда
 */

+"40" + +"2";
/**
 * результат --> 42
 *
 * в даному прикладі маємо унарний плюс і арифметичне додавання
 *
 *              Number('40') + Number('2')
 *              40 + 2 = 42
 */

"10" - 5 === 6;
/**
 * результат --> false
 *
 * '10' перетворюється в 10
 * 10-5=5
 * 5===6 це false
 */

true + false;
/**
 * результат --> 1
 *
 * true конвертується в 1, а false в 0
 * 1+0=1
 */

"4px" - 3;
/**
 * варіант 1:
 * результат --> NaN
 *
 * Number('4px') це NaN
 * NaN - 3 = NaN (будь яка операція з NaN дає в результаті NaN)
 * 
 * 
 * 
 * варіант 2:
 * результат --> 1
 * parseInt("4px") це 4
 * 4-3=1

 */

"4" - 3;
/**
 * результат --> 1
 *
 * Number('4') це 4
 * 4-3=1
 */

"2" + 3 ** 2;
/**
 * результат --> 29
 *
 * конкатенація і піднесення до степеню
 * вищий пріорітет має піднесення до степеню
 * 3**2=9
 * '2'+ 9 = '29'
 */

12 / "6";
/**
 * результат --> 2
 *
 * Number('6') це 6
 * 12/6=2
 */

23 + 42 + "number";
/**
 * результат --> '65number'
 *
 * арифметичне додавання і конкатенація
 * 23+42=65
 * 65 + 'number' = '65number'
 */

"10" + (5 === 6);
/**
 * результат --> '10false'
 *
 * конкатенація і Strict equal
 *
 * вищий пріорітет операції в дужках
 * 5 === 6 це false
 *
 * '10' + false
 */

"number" + 15 + 3;
